/**
 * Destructuring
 * 
 * Allows you to easily extract elements or object properties and
 * store them in variables.
 * 
 * Array Destructuring & Object Destructuring.
 * 
 * Next Gen examples of JavaScript.
 */

// Arrays
let [greet, user] = ['Hello', 'Kristian']

console.log(greet) // Hello
console.log(user) // Kristian

// Objects
let { userName, age } = { userName: 'Kristian', age: 28 }

console.log(userName) // Kristian
console.log(age) // undefined